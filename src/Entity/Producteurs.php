<?php

namespace App\Entity;

use App\Repository\ProducteursRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=ProducteursRepository::class)
 */
class Producteurs
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=70)
     */
    private $prenom;

    /**
     * @ORM\Column(type="string", length=70)
     */
    private $nom;

    /**
     * @ORM\Column(type="string", length=100)
     */
    private $type;

    /**
     * @ORM\Column(type="string", length=100)
     */
    private $ville;

    /**
     * @ORM\Column(type="string", length=10)
     */
    private $departement;

    /**
     * @ORM\Column(type="text")
     */
    private $descriptionProducteur;

    /**
     * @ORM\OneToMany(targetEntity=Produits::class, mappedBy="producteur")
     */
    private $produits;

    public function __construct()
    {
        $this->produits = new ArrayCollection();
    }


    /////////////////////////////////////////////

    public function getId(): ?int
    {
        return $this->id;
    }

    ////////////////////////////////////////////

    public function getPrenom(): ?string
    {
        return $this->prenom;
    }

    public function setPrenom(string $prenom): self
    {
        $this->prenom = $prenom;

        return $this;
    }

    /////////////////////////////////////////////

    public function getNom(): ?string
    {
        return $this->nom;
    }

    public function setNom(string $nom): self
    {
        $this->nom = $nom;

        return $this;
    }
    /////////////////////////////////////////////////
    ///
    public function getType(): ?string
    {
        return $this->type;
    }

    public function setType(string $type): self
    {
        $this->type = $type;

        return $this;
    }

    ////////////////////////////////////////////////

    public function getVille(): ?string
    {
        return $this->ville;
    }

    public function setVille(string $ville): self
    {
        $this->ville = $ville;

        return $this;
    }

    ///////////////////////////////////////////

    public function getDepartement(): ?string
    {
        return $this->departement;
    }

    public function setDepartement(string $departement): self
    {
        $this->departement = $departement;

        return $this;
    }

    ////////////////////////////////////////////

    public function getDescriptionProducteur(): ?string
    {
        return $this->descriptionProducteur;
    }

    public function setDescriptionProducteur(string $descriptionProducteur): self
    {
        $this->descriptionProducteur = $descriptionProducteur;

        return $this;
    }

    //////////////////////////////////////////////////////////////

    /**
     * @return Collection|Produits[]
     */
    public function getProduits(): Collection
    {
        return $this->produits;
    }

    public function addProduit(Produits $produit): self
    {
        if (!$this->produits->contains($produit)) {
            $this->produits[] = $produit;
            $produit->setProducteur($this);
        }

        return $this;
    }

    public function removeProduit(Produits $produit): self
    {
        if ($this->produits->contains($produit)) {
            $this->produits->removeElement($produit);
            // set the owning side to null (unless already changed)
            if ($produit->getProducteur() === $this) {
                $produit->setProducteur(null);
            }
        }

        return $this;
    }
}
